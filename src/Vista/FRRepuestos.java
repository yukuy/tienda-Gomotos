    private void limpiarFORMULARIO() {
        txtidRepuestos.setValue(0);
        txtnombre.setText("escriba nombre");
        txtcantidad.setText("escriba cantidad");
        txttipo.setText("escriba el tipo");
        txtprecio.setText("escriba el precio");
        btmodificar.setEnabled(false);
        bteliminar.setEnabled(false);
        llenarTABLA();
    }

    private void llenarTABLA() {
        Repuestos unrepuesto = new Repuestos();
        DefaultTableModel tabla = (DefaultTableModel)tblrepuestos.getModel();
        Iterator<Repuestos> itrepuestos = unrepuesto.listar();
        Object[] filarepuestos = new Object[tblrepuestos.getColumnCount()];
        tabla.setRowCount(0);
        while (itrepuestos.hasNext()){
            unrepuesto = itrepuestos.next();
            filarepuestos[0] = unrepuesto.getIdRepuestos();
            filarepuestos[1] = unrepuesto.getNombre();
            filarepuestos[2] = unrepuesto.getCantidad();
            filarepuestos[3] = unrepuesto.getTipo();
            filarepuestos[4] = unrepuesto.getPrecio();
            tabla.addRow(filarepuestos);
        }
    }

    private Repuestos obtenerunrep() {
        Repuestos elrepuesto = new Repuestos();
        elrepuesto.setIdRepuestos((Integer) txtidRepuestos.getValue());
        elrepuesto.setNombre( txtnombre.getText());
        elrepuesto.setCantidad( Integer.parseInt(txtcantidad.getText()));
        elrepuesto.setTipo( txttipo.getText());
        elrepuesto.setPrecio( Integer.parseInt(txtprecio.getText()));
        return elrepuesto;
    }

    private void llenarTablaConBuscar(String busqueda) {
         Repuestos unrepuesto = new Repuestos();
        DefaultTableModel tabla = (DefaultTableModel)tblrepuestos.getModel();
        Iterator<Repuestos> itrepuestos = unrepuesto.buscar(busqueda);
        Object[] filarepuestos = new Object[tblrepuestos.getColumnCount()];
        tabla.setRowCount(0);
        while (itrepuestos.hasNext()){
            unrepuesto = itrepuestos.next();
            filarepuestos[0] = unrepuesto.getIdRepuestos();
            filarepuestos[1] = unrepuesto.getNombre();
            filarepuestos[2] = unrepuesto.getCantidad();
            filarepuestos[3] = unrepuesto.getTipo();
            filarepuestos[4] = unrepuesto.getPrecio();
            ((DefaultTableModel) tblrepuestos.getModel()).addRow(filarepuestos);
        }
    }
