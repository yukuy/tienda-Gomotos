    private void tblrepuestosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblrepuestosMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int fila = tblrepuestos.rowAtPoint(evt.getPoint());
            if (fila > -1) {
                txtidRepuestos.setValue(tblrepuestos.getValueAt(fila, 0)); // Suponiendo que txtidRepuestos es un campo de tipo Integer
                txtnombre.setText((String) tblrepuestos.getValueAt(fila, 1));
                int cantidad = (int) tblrepuestos.getValueAt(fila, 2); // Obtener como entero
                txtcantidad.setText(String.valueOf(cantidad)); // Convertir a String antes de asignarlo al campo de texto
                txttipo.setText((String) tblrepuestos.getValueAt(fila, 3));
                int precio = (int) tblrepuestos.getValueAt(fila, 4); // Obtener como entero
                txtprecio.setText(String.valueOf(precio)); // Convertir a String antes de asignarlo al campo de texto
                btmodificar.setEnabled(true);
                bteliminar.setEnabled(true);
            }
        }
    }//GEN-LAST:event_tblrepuestosMouseClicked

